        <h1>Cryptographic Authentication - the next step for privacy and digital security in the Internet</h1>
        <p>
            Every single year there are news about hacked platforms, which are followed by the publication of private user data.
            Unfortunately, it still happens very often that among the hacked platforms are some that store passwords unencrypted.
            For the 21st century, the digital age, this is a mistake for which there are no excuses. <br>
            <br>
            Every time a user provides his or her personal data on a platform, he or she must trust that the platform will process and store the data responsibly.
            Unfortunately, it becomes apparent every year that such responsible handling of sensitive data is not the norm.
            Even tech giants like facebook have apparently stored passwords unencrypted for years, according to
            <a href="https://krebsonsecurity.com/2019/03/facebook-stored-hundreds-of-millions-of-user-passwords-in-plain-text-for-years/">krebsonsecurity, 2019</a>. <br>
            <br>
            At C-Auth, we believe that security must always be a priority. That is why we firmly believe that such cases of published passwords should no longer exist.
            Furthermore, it is not the platforms that should have control over personal data, but the users.
            For this purpose, we are developing C-Auth. We have set ourselves goals that we want to achieve when using C-Auth:<br>
        </p>
